                <h2>Tingkatkan Literasi dan Kapasitas TBM di DKI Jakarta, Kompas Gramedia Donasikan Buku Melalui Forum TBM DKI Jakarta</h2>
                <br>
                    <p class="rilis">Jum'at, 11 November 2022.</p>

                    <img src="../../dist/img/img/img08.jpg" width="550px">

                    <p>Sesuai dengan visi misi Kompas Gramedia dalam mencerdaskan kehidupan bangsa, Kompas Gramedia turut serta berkontribusi dalam kegiatan everybody’s Reading yang diselenggarakan oleh forum TBM Jakarta dalam upaya peningkatan literasi. Melalui kegiatan program donasi #AkuBaca, Kompas Gramedia mendonasikan 150 eksemplar buku anak (10/11/2022) sebagai wujud kepedulian dan kontribusi aktif perusahaan dalam meningkatkan literasi masyarakat, membuka jendela pengetahuan, sekaligus memperkuat relasi antar pihak. Buku yang didonasikan nantinya akan disalurkan oleh Forum TBM DKI Jakarta ke taman bacaan dan komunitas yang menjadi jejaring Forum TBM DKI jakarta.</p>

                    <p>Forum Taman Bacaan Masyarakat (Forum TBM) DKI Jakarta merupakan sebuah wadah untuk berhimpun dan berorganisasi bagi para pendiri dan pengelola Taman Bacaan Masyarakat (TBM), pegiat literasi, komunitas dan masyarakat. TBM-TBM di DKI Jakarta diwadahi untuk bisa saling berkolaborasi dan mengajak stakeholders yang sejalan. Tujuannya yaitu untuk bersama-sama mengembangkan gerakan minat baca, literasi, dan pendidikan masyarakat serta meningkatkan kapasitas pengelolaan TBM, dan penggiat literasi khususnya di daerah DKI Jakarta.  Oleh karena itu, untuk mencapai tujuan dan visi misinya, Forum TBM DKI Jakarta bersinergi dengan 70 TBM di Jakarta sebagai simpul jejaring mengadakan kegiatan Everybody’s Reading (Festival Taman Baca dan Komunitas Literasi). Festival ini merupakan sebuah acara yang pertama kali dilakukan oleh Forum TBM DKI Jakarta, di mana dua periode sebelumnya tidak pernah ada. Kegiatan ini diselenggarakan di Taman Ismail Marzuki (TIM) dalam bentuk workshop, diskusi, serta aksi bersama dalam bentuk donasi buku anak (Buku Anak untuk Jakarta) dan donasi untuk gerakan literasi Jakarta (10-13/11/2022).</p>
                        
                    <p>Bantuan program AkuBaca disambut baik oleh Ketua Forum TBM Yudy Hartanto  “Terima kasih kepada Kompas Gramedia, program donasi #AkuBaca sangat bermanfaat sekali untuk taman baca. Hal ini membuktikan hadirnya peran swasta dalam mendukung gerakan literasi di masyarakat. Diharapkan peran Kompas Gramedia sebagai mitra Forum TBM akan terus berkelanjutan sehingga para pengelola TBM akan terus terjaga semangatnya dalam ikut mencerdaskan bangsa".</p>
                        
                    <p>Dalam kesempatan yang sama, CSR Analyst Kompas Gramedia Mochamad Ardiyanto juga menyampaikan pentingnya peningkatan literasi melalui pengadaan bahan bacaan bagi masyarakat. "Terima kasih juga kepada semua pihak yang bersama-sama menguatkan literasi, rasanya misi meningkatkan literasi memang perlu banyak pihak yang terlibat untuk mendorong masyarakat agar terus semangat membuat kegiatan-kegiatan yang menciptakan ruang pengetahuan baru bagi masyarakat, terutama untuk anak-anak sebagai generasi bangsa," ujarnya.</p>

