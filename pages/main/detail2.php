                    <p>Rabu, 9 November 2022 - Kompas Gramedia menerima kunjungan dari PT. Matahari Graha Fantasi (TimeZone) secara luring pada Rabu siang (9/11/2022). Pertemuan ini berlangsung di Ruang Borobudur Lantai 6 Menara Kompas yang dihadiri sejumlah perwakilan penting dari PT Matahari Graha Fantasi, yaitu CEO PT. Matahari Graha Fantasi Naveen H., General Manager Marketing PT. Matahari Graha Fantasi Kikie Randini, Head of Marketing Timezone Indonesia Rella Putra Bayunoto, dan Communications and Events Timezone Indonesia Adhityaswara N.</p>

                    <p>Kunjungan disambut hangat oleh Kompas Gramedia sebagai bentuk antusiasme jumpa kali pertama. Turut hadir dalam kunjungan ini Direktur Kerja Sama Antarlembaga Kompas Rusdi Amral, Corporate Communication Director KG Glory Oyong, GM Communication Management and Bentara Budaya Ilham Khoiri, GM Customer Facing Platform MyValue Vebri Anggara, Digital Manager Kompas TV Haris Mahardiansyah, News Manager Tribunnews Rachmat Hidayat, Editor in Chief Children Media Grid Network Lucia Triundari, Editor in Chief Nakita.id David Togatorop, Editor in Chief HAI Alvin Bahar, Managing Editor GridGames Kama Adritya, PR Manager GoRP Rezza Patria Wibowo, Head of PR KG Media Arrozi Effendi, Account Manager KG Radio Network Embun Hendrodjati, dan Editor bobo.id Iveta Rahmalia.</p>
                        
                    <p>General Manager Communication Management & Bentara Budaya Ilham Khoiri  membuka pertemuan dengan memperkenalkan Kompas Gramedia secara singkat sekaligus memperkenalkan jajaran pimpinan yang turut hadir. “Terima kasih sudah hadir dan berkunjung ke Kompas Gramedia. Kami sangat menyambut dengan baik dan melihat ke depan terkait potensi kolaborasi apa yang dapat kita wujudkan,” ujar Ilham.</p>
                        
                    <p>Di samping itu, Glory Oyong juga menambahkan sudut pandangnya terkait TimeZone. “Seperti yang kita ketahui, TimeZone hadir menemani masa kecil kita semua. Saya rasa banyak yang dapat dikolaborasikan ke depannya, sekaligus kami ingin mengetahui apa yang baru dari TimeZone era ke era.”</p>
                        
                    <p>CEO PT Matahari Graha Fantasi menjawab bahwa hampir seluruh orang di Indonesia sudah mendengar dan tidak asing dengan TimeZone. TimeZone hadir untuk menciptakan kebahagiaan dan memorable moment bagi pengunjung. Saat ini, kami menawarkan beberapa hal baru untuk mendukung guests saling terkoneksi seperti pembaruan konten dan jenis arcades games, seperti bowling spaces. Di samping itu juga dihadirkan private/party spaces untuk menciptakan senyum, tawa, dan keseruan untuk para pengunjung.</p>
                        
                    <p>Pada sesi diskusi, perwakilan dari unit Kompas Gramedia juga menyampaikan beberapa ide terkait bentuk kerja sama agar nantinya bisa dibicarakan lebih lanjut pada kesempatan berikutnya. Selanjutnya, acara ditutup dengan pemberian cendera mata dan sesi foto bersama. Dengan diakhirnya sesi foto, maka berakhir pula pertemuan ini.</p> 
                        
                    <p>Jumpa pertama ini diharapkan dapat menjadi cikal bakal kerja sama antara Kompas Gramedia dan PT. Matahari Graha Fantasi (TimeZone Indonesia). Hal detil yang akan mendorong kerja sama sangat dinantikan oleh Kompas Gramedia. Glory Oyong juga memberi masukan gambaran kerja sama seperti penawaran item Gramedia di Merchandise Area TimeZone, penyediaan tempat untuk event dan community gathering, dan penawaran menarik lainnya antara Kompas Gramedia dan TimeZone.</p>

